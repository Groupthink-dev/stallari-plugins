#!/usr/bin/env node
/**
 * Build Forge context — reads service contracts, agent roster, and pack
 * manifests, then generates TypeScript/JS modules consumed by the Forge
 * prompts and validators.
 *
 * Reads:   schemas/contracts/*.json
 *          data/agents.yaml
 *          plugins/packs/*.yaml
 * Writes:  worker/src/prompts/generated/service-vocabulary.ts
 *          worker/src/prompts/generated/ecosystem-vocabulary.ts
 *          worker/src/prompts/generated/declared-services.ts
 *          scripts/generated/declared-services.js
 *
 * Usage: node scripts/build-forge-context.js
 */

import { readdir, readFile, mkdir, writeFile, stat } from "node:fs/promises";
import { join, resolve } from "node:path";
import { parse as parseYAML } from "yaml";

const ROOT = resolve(import.meta.dirname, "..");
const CONTRACTS_DIR = join(ROOT, "schemas", "contracts");
const AGENTS_FILE = join(ROOT, "data", "agents.yaml");
const PACKS_DIR = join(ROOT, "plugins", "packs");
const PRIVATE_PACKS_DIR = process.env.PRIVATE_PACKS_DIR || null;
const WORKER_GEN_DIR = join(ROOT, "worker", "src", "prompts", "generated");
const SCRIPTS_GEN_DIR = join(ROOT, "scripts", "generated");

/** Services most users have installed — presented first in Forge prompts. */
const CORE_SERVICES = new Set(["email", "tasks", "calendar", "vault"]);

/** Services that appear in DECLARED_SERVICES but have no contract yet. */
const ADDITIONAL_SERVICES = ["registry", "dns", "storage"];

/** Classification display order. */
const CLASSIFICATION_ORDER = ["required", "recommended", "optional", "gated"];

async function loadContracts() {
  const files = await readdir(CONTRACTS_DIR);
  const contracts = [];

  for (const file of files.sort()) {
    if (!file.endsWith(".json")) continue;
    const raw = await readFile(join(CONTRACTS_DIR, file), "utf-8");
    const contract = JSON.parse(raw);
    if (!contract.operations || !contract.properties?.service?.const) continue;
    contracts.push({
      file,
      id: contract.title || contract.properties.name?.const,
      service: contract.properties.service.const,
      version: contract.properties.version?.const,
      operations: contract.operations,
    });
  }

  return contracts.sort((a, b) => a.service.localeCompare(b.service));
}

/**
 * Format a single contract into a one-line service description for the Forge
 * scope prompt. Matches the existing format:
 *   **email** (email-v1): search, read, mailboxes, ... | write-gated: send, reply, ...
 */
function formatServiceLine(contract) {
  const grouped = {};
  for (const cls of CLASSIFICATION_ORDER) grouped[cls] = [];
  for (const op of contract.operations) {
    const cls = op.classification || "optional";
    if (grouped[cls]) grouped[cls].push(op.name);
  }

  const readOps = [
    ...grouped.required,
    ...grouped.recommended,
    ...grouped.optional,
  ].filter(Boolean);

  const gatedOps = grouped.gated;

  let line = `**${contract.service}** (${contract.id}): ${readOps.join(", ")}`;
  if (gatedOps.length > 0) {
    line += ` | write-gated: ${gatedOps.join(", ")}`;
  }
  return line;
}

function buildServiceVocabularyTS(contracts) {
  const coreContracts = contracts.filter((c) => CORE_SERVICES.has(c.service));
  const extContracts = contracts.filter((c) => !CORE_SERVICES.has(c.service));

  const coreLines = coreContracts.map(formatServiceLine);
  const extLines = extContracts.map(formatServiceLine);

  const coreList = coreContracts.map((c) => `"${c.service}"`).join(", ");
  const extList = extContracts.map((c) => `"${c.service}"`).join(", ");

  let vocab = "### Core services (most users have these)\n\n";
  vocab += coreLines.join("\n");
  if (extLines.length > 0) {
    vocab +=
      "\n\n### Extension services (require additional MCP installation)\n\n";
    vocab += extLines.join("\n");
  }

  return `// AUTO-GENERATED by scripts/build-forge-context.js — do not edit
// Generated from ${contracts.length} contract files in schemas/contracts/

export const SERVICE_VOCABULARY = \`${vocab}\`;

export const CORE_SERVICES = [${coreList}] as const;

export const EXTENSION_SERVICES = [${extList}] as const;
`;
}

function buildDeclaredServicesTS(contracts) {
  const serviceNames = [
    ...new Set([
      ...contracts.map((c) => c.service),
      ...ADDITIONAL_SERVICES,
    ]),
  ].sort();

  const list = serviceNames.map((s) => `  "${s}",`).join("\n");

  return `// AUTO-GENERATED by scripts/build-forge-context.js — do not edit
// Union of contract-derived services + additional declared services

export const DECLARED_SERVICES: string[] = [
${list}
];
`;
}

function buildDeclaredServicesJS(contracts) {
  const serviceNames = [
    ...new Set([
      ...contracts.map((c) => c.service),
      ...ADDITIONAL_SERVICES,
    ]),
  ].sort();

  const list = serviceNames.map((s) => `  "${s}",`).join("\n");

  return `// AUTO-GENERATED by scripts/build-forge-context.js — do not edit
// Union of contract-derived services + additional declared services

export const DECLARED_SERVICES = [
${list}
];
`;
}

// ---------------------------------------------------------------------------
// Ecosystem vocabulary (agents + packs)
// ---------------------------------------------------------------------------

async function loadAgents() {
  try {
    const raw = await readFile(AGENTS_FILE, "utf-8");
    const doc = parseYAML(raw);
    return doc?.agents ?? {};
  } catch {
    console.warn("  ⚠ data/agents.yaml not found — skipping agent roster");
    return {};
  }
}

async function loadPacksFromDir(dir) {
  const packs = [];
  try {
    const files = await readdir(dir);
    for (const file of files.sort()) {
      if (!file.endsWith(".yaml") && !file.endsWith(".yml")) continue;
      const raw = await readFile(join(dir, file), "utf-8");
      const doc = parseYAML(raw);
      if (!doc?.pack || !doc?.name) continue;

      packs.push(packDocToEntry(doc));
    }
  } catch {
    // Directory not readable — skip silently
  }
  return packs;
}

/**
 * Read pre-sealed pack artifacts from a directory structured as:
 *   {dir}/{slug}/{version}/manifest.json
 *
 * Sealed manifests have prompts replaced with "[sealed]" but all metadata
 * (name, description, skills list, services, etc.) is preserved.
 */
async function loadSealedPacksFromDir(dir) {
  const packs = [];
  try {
    const slugs = await readdir(dir);
    for (const slug of slugs.sort()) {
      const slugDir = join(dir, slug);
      const slugStat = await stat(slugDir).catch(() => null);
      if (!slugStat?.isDirectory()) continue;

      const versions = await readdir(slugDir);
      for (const version of versions.sort()) {
        const packDir = join(slugDir, version);
        const versionStat = await stat(packDir).catch(() => null);
        if (!versionStat?.isDirectory()) continue;

        try {
          const raw = await readFile(join(packDir, "manifest.json"), "utf-8");
          const doc = JSON.parse(raw);
          if (!doc?.name) continue;
          packs.push(packDocToEntry(doc));
        } catch {
          continue;
        }
      }
    }
  } catch {
    // Directory not readable — skip silently
  }
  return packs;
}

/** Extract a forge-vocabulary pack entry from a pack document (YAML or sealed manifest). */
function packDocToEntry(doc) {
  const skills = Array.isArray(doc.skills) ? doc.skills : [];
  const agents = doc.agents ? Object.keys(doc.agents) : [];
  const services = extractPackServices(doc);
  const visibility = doc.visibility ?? "open";
  const tier = doc.tier ?? "community";
  const pricing = doc.pricing;
  const isFree = !pricing || pricing.model === "free" || pricing === null;

  return {
    name: doc.name,
    version: doc.version ?? "1.0.0",
    description: doc.description ?? "",
    skillCount: skills.length,
    agentCount: agents.length,
    agents,
    services,
    visibility,
    tier,
    isFree,
    pricingLabel: isFree ? "free" : `${pricing.model} $${pricing.amount}/${pricing.interval ?? "once"}`,
    isBundled: !!doc.bundled,
  };
}

async function loadPacks() {
  const packs = await loadPacksFromDir(PACKS_DIR);
  if (PRIVATE_PACKS_DIR) {
    const sealedPacks = await loadSealedPacksFromDir(PRIVATE_PACKS_DIR);
    if (sealedPacks.length > 0) {
      console.log(`  Loaded ${sealedPacks.length} pre-sealed pack(s) from PRIVATE_PACKS_DIR`);
    }
    packs.push(...sealedPacks);
  }
  return packs;
}

/** Extract service names from a pack manifest (requires or recommends). */
function extractPackServices(doc) {
  const services = new Set();
  const blocks = [doc.requires, doc.recommends].filter(Boolean);
  for (const block of blocks) {
    if (Array.isArray(block.services)) {
      for (const s of block.services) {
        if (typeof s === "string") services.add(s);
        else if (s?.service) services.add(s.service);
      }
    }
  }
  // Also scan skills for services_used
  if (Array.isArray(doc.skills)) {
    for (const skill of doc.skills) {
      if (Array.isArray(skill.services_used)) {
        for (const s of skill.services_used) {
          if (typeof s === "string") services.add(s);
          else if (s?.service) services.add(s.service);
        }
      }
    }
  }
  return [...services].sort();
}

/**
 * Format agent roster + pack catalog into a compact vocabulary for the Forge
 * scope prompt. Token-efficient: one line per agent, two lines per pack.
 */
function buildEcosystemVocabularyTS(agents, packs) {
  const sections = [];

  // Agent roster
  const agentEntries = Object.entries(agents);
  if (agentEntries.length > 0) {
    const lines = ["### Platform agents\n"];
    lines.push("These are the built-in operator agents. Packs assign skills to agents. When a user's workflow aligns with an existing agent's domain, use that agent rather than inventing a new one.\n");
    for (const [name, agent] of agentEntries) {
      const svcs = (agent.services ?? []).join(", ");
      lines.push(`**${name}** (${agent.role}) [${svcs}]: ${agent.domain}`);
    }
    sections.push(lines.join("\n"));
  }

  // Pack catalog
  const openPacks = packs.filter((p) => p.visibility === "open" && !p.isBundled);
  const sealedPacks = packs.filter((p) => p.visibility === "sealed");
  const bundledPacks = packs.filter((p) => p.isBundled);

  if (packs.length > 0) {
    const lines = ["\n### Existing packs\n"];
    lines.push("Reference these when the user's needs overlap. Suggest forking an open pack rather than building from scratch. Mention sealed/paid packs as alternatives when relevant.\n");

    if (bundledPacks.length > 0) {
      lines.push("**Bundled (included with Stallari):**");
      for (const p of bundledPacks) {
        lines.push(formatPackLine(p));
      }
      lines.push("");
    }

    if (openPacks.length > 0) {
      lines.push("**Open (forkable):**");
      for (const p of openPacks) {
        lines.push(formatPackLine(p));
      }
      lines.push("");
    }

    if (sealedPacks.length > 0) {
      lines.push("**Sealed (proprietary):**");
      for (const p of sealedPacks) {
        lines.push(formatPackLine(p));
      }
    }

    sections.push(lines.join("\n"));
  }

  const vocabulary = sections.join("\n");

  return `// AUTO-GENERATED by scripts/build-forge-context.js — do not edit
// Generated from data/agents.yaml + ${packs.length} pack manifests

export const ECOSYSTEM_VOCABULARY = \`${vocabulary}\`;

export const AGENT_NAMES = [${agentEntries.map(([n]) => `"${n}"`).join(", ")}] as const;

export const PACK_NAMES = [${packs.map((p) => `"${p.name}"`).join(", ")}] as const;
`;
}

function formatPackLine(pack) {
  const svcs = pack.services.join(", ");
  const meta = [`${pack.skillCount} skills`];
  if (pack.agentCount > 0) meta.push(`${pack.agentCount} agents`);
  meta.push(pack.pricingLabel);
  return `- **${pack.name}** v${pack.version} [${svcs}]: ${truncate(pack.description, 100)} (${meta.join(", ")})`;
}

function truncate(str, max) {
  if (!str || str.length <= max) return str || "";
  return str.slice(0, max - 1) + "…";
}

async function main() {
  const contracts = await loadContracts();
  console.log(
    `  Read ${contracts.length} contracts: ${contracts.map((c) => c.id).join(", ")}`,
  );

  // Ensure output directories exist
  await mkdir(WORKER_GEN_DIR, { recursive: true });
  await mkdir(SCRIPTS_GEN_DIR, { recursive: true });

  // Load ecosystem data (agents + packs)
  const agents = await loadAgents();
  const packs = await loadPacks();
  const agentCount = Object.keys(agents).length;
  console.log(`  Read ${agentCount} agents, ${packs.length} packs`);

  // Generate files
  const vocabTS = buildServiceVocabularyTS(contracts);
  const ecosystemTS = buildEcosystemVocabularyTS(agents, packs);
  const declaredTS = buildDeclaredServicesTS(contracts);
  const declaredJS = buildDeclaredServicesJS(contracts);

  await writeFile(join(WORKER_GEN_DIR, "service-vocabulary.ts"), vocabTS);
  await writeFile(join(WORKER_GEN_DIR, "ecosystem-vocabulary.ts"), ecosystemTS);
  await writeFile(join(WORKER_GEN_DIR, "declared-services.ts"), declaredTS);
  await writeFile(join(SCRIPTS_GEN_DIR, "declared-services.js"), declaredJS);

  console.log(`  Generated worker/src/prompts/generated/service-vocabulary.ts`);
  console.log(`  Generated worker/src/prompts/generated/ecosystem-vocabulary.ts`);
  console.log(`  Generated worker/src/prompts/generated/declared-services.ts`);
  console.log(`  Generated scripts/generated/declared-services.js`);

  // Summary
  const all = [
    ...new Set([
      ...contracts.map((c) => c.service),
      ...ADDITIONAL_SERVICES,
    ]),
  ].sort();
  console.log(`  DECLARED_SERVICES (${all.length}): ${all.join(", ")}`);
}

main().catch((err) => {
  console.error("build-forge-context failed:", err);
  process.exit(1);
});
